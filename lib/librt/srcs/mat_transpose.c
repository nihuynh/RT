#include "rtstruct.h"

void	mat_transpose(t_matrix *m)
{
	t_matrix tmp;

	tmp.m[0][0] = m->m[0][0];
	tmp.m[0][1] = m->m[1][0];
	tmp.m[0][2] = m->m[2][0];
	tmp.m[1][0] = m->m[0][1];
	tmp.m[1][1] = m->m[1][1];
	tmp.m[1][2] = m->m[2][1];
	tmp.m[2][0] = m->m[0][2];
	tmp.m[2][1] = m->m[1][2];
	tmp.m[2][2] = m->m[2][2];
	*m = tmp;
}
